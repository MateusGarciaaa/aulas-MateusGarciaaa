    public static void processarMostrarIntervalo(int inicio, int fim) {
        int indice = inicio;
        int incremento = 1;

        if (inicio > fim) {
            incremento = -1;
        }

        while (indice != fim + incremento) {
            if (indice == inicio) {
                IO.imprimir("\n Os valores do intervalo são: ");
            }
            IO.imprimirValorInteiro(indice);
            indice = indice + incremento;
        }
    }

