    // RECEBE E CONFERE OS INTERVALOS DOS NUMEROS DIGITADOS
    public static int[] conferirIntervalos(int fimIntervaloUm, int fimIntervaloDois, int fimIntervaloTres) {
        int qtdNumTotal = conferirQuantidaDeNumerosDigitados();
        int qtdNumDigitados = 0;
        int intervaloUm = 0;
        int intervaloDois = 0;
        int intervaloTres = 0;

        while (qtdNumDigitados < qtdNumTotal) {
            int valor = conferirIntervalo(0, 2147483647, "");
            if (valor <= fimIntervaloUm) {
                intervaloUm++;
            } else if (valor <= fimIntervaloDois) {
                intervaloDois++;
            } else {
                intervaloTres++;
            }
            qtdNumDigitados++;
        }
        return new int[] { intervaloUm, intervaloDois, intervaloTres };

    }

