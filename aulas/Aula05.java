    // CONFERE SE O NUMERO É POSITIVO OU NEGATIVO
    public static int[] conferidorPosNeg(int qtdNum) {
        int qtdPos = 0;
        int qtdNeg = 0;
        int qtdZero = 0;

        for (int i = 0; i < qtdNum; i++) {
            IO.imprimir("Digite o " + (i + 1) + "º número:");
            int numero = IO.lerValorInteiro();

            if (numero > 0) {
                qtdPos++;
            } else if (numero < 0) {
                qtdNeg++;
            } else {
                qtdZero++;
            }
        }
        return new int[] { qtdPos, qtdNeg, qtdZero };
    }

