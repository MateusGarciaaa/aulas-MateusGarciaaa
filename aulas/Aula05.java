    // SOMA OS VALORES DENTRO DE UM INTERVALO
    public static int somaIntervalo(int inicio, int fim) {
        int soma = 0;
        for (int i = inicio; i <= fim; i++) {
            soma = soma + i;
        }
        IO.imprimirValorInteiro(soma);
        return soma;
    }

    // SOMA OS VALORES PARES DENTRO DE UM INTERVALO
    public static void somaParesOuImparesIntervalo(int inicio, int fim, boolean somarPares, boolean somarImpares) {
        int somaParesIntervalo = 0;
        int somaImparesIntervalo = 0;
        for (int i = inicio; i <= fim; i++) {
            if (i % 2 == 0) {
                somaParesIntervalo = somaParesIntervalo + i;
            } else {
                somaImparesIntervalo = somaImparesIntervalo + i;
            }
        }
        if (somarPares) {
            IO.imprimirValorInteiro(somaParesIntervalo);
        }
        if (somarImpares) {
            IO.imprimirValorInteiro(somaImparesIntervalo);
        }
    }

