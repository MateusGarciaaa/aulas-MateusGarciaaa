    // RECEBE E CONFERE SE O VALOR ESTA DENTRO DO INTERVALO
    public static int conferirIntervalo(int inicio, int fim, String mensagemSolicitacao) {
        int valor = 0;
        do {
            valor = solicitaValorInteiro(mensagemSolicitacao);
            if (valor < inicio || valor > fim) {
                IO.imprimir("Erro: valor fora do intervalo (" + inicio + " a " + fim + ").");
            }

        } while (valor < inicio || valor > fim);
        return valor;
    }

