    // CALCULAR OS NUMEROS DIVISORES
    public static int calcularDivisores(int inicio, int restoDivisaoDesejado) {
        int numero = receberInteiro();
        int somaDivisores = 0;
        for (int divisor = inicio; divisor < numero; divisor++) {
            if (numero % divisor == restoDivisaoDesejado) {
                IO.imprimirValorInteiro(divisor);
                somaDivisores = somaDivisores + divisor;
            }
        }
        return somaDivisores;
    }

    // VERIFICAR SE O NUMERO É PRIMO
    public static boolean primo(int numero) {
        int divisor = 2;
        if (numero <= 1) {
            return false;
        }
        for (int indice = 0; indice < numero; indice++) {
            if (numero % divisor == 0) {
                return false;
            }
            divisor++;
        }
        return true;
    }

    // SOMA OS DIVISORES PERFEITOS
    public static int somaDivisores(int numero) {
        int soma = 0;
        for (int indice = 1; indice < numero; indice++) {
            if (numero % indice == 0) {
                soma = soma + indice;
            }
        }
        return soma;
    }

    // SOMA PARA CALCULAR SE O NUMERO É PERFEITO
    public static boolean calcularNumeroPerfeito(int numero) {
        int soma = somaDivisores(numero);
        return numero == soma;
    }

